-- 1. 确保 site_config 表存在 (如果之前没有创建过)
CREATE TABLE IF NOT EXISTS public.site_config (
    id int8 PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    key text UNIQUE NOT NULL,
    value jsonb NOT NULL,
    created_at timestamptz DEFAULT now(),
    updated_at timestamptz DEFAULT now()
);

-- 2. 开启 RLS
ALTER TABLE public.site_config ENABLE ROW LEVEL SECURITY;

-- 3. 创建 RLS 策略 (如果不存在)
DO $$ 
BEGIN
    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'Allow public read for site_config') THEN
        CREATE POLICY "Allow public read for site_config" ON public.site_config FOR SELECT USING (true);
    END IF;

    IF NOT EXISTS (SELECT 1 FROM pg_policies WHERE policyname = 'Allow auth update for site_config') THEN
        CREATE POLICY "Allow auth update for site_config" ON public.site_config FOR ALL USING (auth.role() = 'authenticated');
    END IF;
END $$;

