-- 5. 确保首页 Tab 配置包含 "造价计算" (可选手动更新，也可通过此脚本)
-- 注意：这里会尝试更新已有的 home_tabs 配置，添加 cost 页签
DO $$
DECLARE
    current_tabs jsonb;
BEGIN
    SELECT value INTO current_tabs FROM public.site_config WHERE key = 'home_tabs';
    
    IF current_tabs IS NOT NULL AND NOT (current_tabs @> '[{"id": "cost"}]') THEN
        UPDATE public.site_config 
        SET value = current_tabs || '[{"id": "cost", "name": "造价计算", "hidden": false}]'
        WHERE key = 'home_tabs';
    END IF;
END $$;
